package _20230305

fun main() = java.io.StreamTokenizer(System.`in`.bufferedReader()).run {
    data class Pos(val x: Int, val y: Int, val z: Int)

    val r = { nextToken();nval.toInt() }
    val m = r()
    val n = r()
    val board = Array(m) { IntArray(n) { r() } }
    val visited = Array(m) { Array(n) { BooleanArray(4) } }
    val queue = ArrayDeque<Pos>()

    fun visit(pos: Pos) = pos.run {
        if (x in 0 until n && y in 0 until m && board[y][x] == 0) {
            if (visited[y][x][z].not()) {
                visited[y][x][z] = true
                queue.add(pos)
            }
            true
        } else {
            false
        }
    }

    visit(Pos(y = r() - 1, x = r() - 1, z = r() - 1))
    val goal = Pos(y = r() - 1, x = r() - 1, z = r() - 1)

    var count = 0
    while (queue.isNotEmpty()) {
        repeat(queue.size) {
            val now = queue.removeFirst()
            if (now == goal) return print(count)
            for (d in 1..3) {
                val next = now.run {
                    when (z) {
                        0 -> Pos(x + d, y, z)
                        1 -> Pos(x - d, y, z)
                        2 -> Pos(x, y + d, z)
                        else -> Pos(x, y - d, z)
                    }
                }
                if (visit(next).not()) {
                    break
                }
            }
            for (d in intArrayOf(0, 1)) {
                val next = now.run {
                    when (z) {
                        0, 1 -> Pos(x, y, 2 + d)
                        else -> Pos(x, y, d)
                    }
                }
                visit(next)
            }
        }
        count++
    }
}

